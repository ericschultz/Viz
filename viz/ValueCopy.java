package viz;

public class ValueCopy {
	private int value;
	private String textId;
	
	public int getValue()
	{
		return value;
	}
	
	public void setValue(int value)
	{
		this.value = value;
	}
	
	public String getTextId()
	{
		return textId;
	}
	
	public void setTextId(String textId)
	{
		this.textId = textId;
	}
}
